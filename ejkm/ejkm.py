def getsimgLoc(simg_name):
    if "docker://" in simg_name.lower():
        res = autoBuildSingularityImagefromDockerRepository(simg_name) # sandbox will be supported 20230816
    else:
        res = os.path.join(os.getcwd(),simg_name)
